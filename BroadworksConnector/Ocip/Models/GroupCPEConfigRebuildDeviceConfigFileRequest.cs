        [XmlElement(ElementName = "force", IsNullable = false, Namespace = "")]
        public bool Force {
            get => _force;
            set {
                ForceSpecified = true;
                _force = value;
            }
        }

        [XmlIgnore]
        public bool ForceSpecified { get; set; }
        
    }
}
